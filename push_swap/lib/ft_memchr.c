#include "libft.h"

char	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		if (*ptr == (unsigned char) c)
			return ((void *)ptr);
		i++;
		ptr++;
	}
	ptr = NULL;
	return ((void *)ptr);
}
